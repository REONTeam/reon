	for ($i = 0; $i < 16; $i++) {
		for ($j = 0; $j < 8; $j++) {
			$symbol = (ord($data[$i * 9 + $j + 4]) >> $j
				| ord($data[$i * 9 + $j + 5]) << (8 - $j)) & 0x1FF;
			if (!array_key_exists($symbol, $decode_table)) {
				http_response_code(400);
				return;
			}
			$decoded = $decoded.chr($decode_table[$symbol]);
		}
	}

	$config = getConfig();

	$ident = substr($decoded, 0, 0x40);
	$email = rtrim(substr($ident, 0, 0x1E), "0");
	$date = rtrim(substr($ident, 0x1E), "0");

	if (substr($email, 8) !== "@".$config["email_domain_dion"]) {
		http_response_code(400);
		return;
	}

	$db = connectMySQL();
	$stmt = $db->prepare("select dion_email_local from sys_users where id = ?");
	$stmt->bind_param("i", $_SESSION["userId"]);
	$stmt->execute();
	$result = fancy_get_result($stmt);
	if (sizeof($result) == 0 || $result[0]["dion_email_local"] !== substr($email, 0, 8)) {
		http_response_code(400);
		return;
	}

	if (substr($date, 25) !== " GMT") {
		http_response_code(400);
		return;
	}

	$datetime = date_create_immutable_from_format("D,\\ d\\ M\\ Y\\ H:i:s\\ e", $date);
	if ($datetime === false || $datetime >= date_create_immutable()) {
		http_response_code(400);
